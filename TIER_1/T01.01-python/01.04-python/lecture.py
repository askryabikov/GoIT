# 3. | ‚Äî –∞–ª—å—Ç–µ—Ä–Ω–∞—Ü–∏—è (¬´–∏–ª–∏¬ª)
# 4. +?, *? ‚Äî –Ω–µ–∂–∞–¥–Ω—ã–µ –≤–µ—Ä—Å–∏–∏, –∑–∞—Ö–≤–∞—Ç—ã–≤–∞—é—Ç –º–∏–Ω–∏–º–∞–ª—å–Ω–æ –≤–æ–∑–º–æ–∂–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–∏–º–≤–æ–ª–æ–≤

import re

# a* ‚Äî –Ω–æ–ª—å –∏–ª–∏ –±–æ–ª—å—à–µ –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π "a"
print(re.findall(r"a*", "caaabb"))      # ['', 'aaa', '', '', '']
# –æ–±—ä—è—Å–Ω–µ–Ω–∏–µ: –º–µ–∂–¥—É —Å–∏–º–≤–æ–ª–∞–º–∏ —Ç–æ–∂–µ –µ—Å—Ç—å ¬´–ø—É—Å—Ç—ã–µ¬ª —Å–æ–≤–ø–∞–¥–µ–Ω–∏—è
# ¬´–ù–∞–π–¥–∏ –≤—Å–µ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏, –≥–¥–µ –±—É–∫–≤–∞ a –≤—Å—Ç—Ä–µ—á–∞–µ—Ç—Å—è –Ω–æ–ª—å –∏–ª–∏ –±–æ–ª—å—à–µ —Ä–∞–∑ –ø–æ–¥—Ä—è–¥¬ª.


# a+ ‚Äî –æ–¥–Ω–æ –∏–ª–∏ –±–æ–ª—å—à–µ –ø–æ–≤—Ç–æ—Ä–µ–Ω–∏–π "a"
print(re.findall(r"a+", "caaabb"))      # ['aaa']
# –æ–±—ä—è—Å–Ω–µ–Ω–∏–µ: –∏—â–µ—Ç –ø–æ–¥—Ä—è–¥ –∏–¥—É—â–∏–µ "a", –Ω–æ –Ω–µ –ø—É—Å—Ç—ã–µ –º–µ—Å—Ç–∞

# a? ‚Äî –Ω–æ–ª—å –∏–ª–∏ –æ–¥–Ω–æ –≤—Ö–æ–∂–¥–µ–Ω–∏–µ "a"
print(re.findall(r"a?", "caaabb"))      # ['','a','a','a','','']
# –æ–±—ä—è—Å–Ω–µ–Ω–∏–µ: –¥–ª—è –∫–∞–∂–¥–æ–π –ø–æ–∑–∏—Ü–∏–∏ –≤–æ–∑–≤—Ä–∞—â–∞–µ—Ç –º–∞–∫—Å–∏–º—É–º –æ–¥–Ω—É "a"

# a{5} ‚Äî —Ä–æ–≤–Ω–æ 5 "a"
print(re.findall(r"a{5}", "aaaaaabaaaa"))  # ['aaaaa']
# –æ–±—ä—è—Å–Ω–µ–Ω–∏–µ: —Ç–æ–ª—å–∫–æ –ø–æ—Å–ª–µ–¥–æ–≤–∞—Ç–µ–ª—å–Ω–æ—Å—Ç–∏ –∏–∑ 5 –ø–æ–¥—Ä—è–¥

# a{2,} ‚Äî –¥–≤–∞ –∏–ª–∏ –±–æ–ª—å—à–µ "a"
print(re.findall(r"a{2,}", "a aa aaa aaaa"))  # ['aa', 'aaa', 'aaaa']
# –æ–±—ä—è—Å–Ω–µ–Ω–∏–µ: –∏—â–µ—Ç –≥—Ä—É–ø–ø—ã "a" –¥–ª–∏–Ω–æ–π ‚â• 2

# a{1,3} ‚Äî –æ—Ç –æ–¥–Ω–æ–π –¥–æ —Ç—Ä—ë—Ö "a"
print(re.findall(r"a{1,3}", "aaaaa"))  # ['aaa', 'aa']
# –æ–±—ä—è—Å–Ω–µ–Ω–∏–µ: ¬´–∂–∞–¥–Ω—ã–π¬ª —Ä–µ–∂–∏–º ‚Äî –∑–∞–±–∏—Ä–∞–µ—Ç –º–∞–∫—Å–∏–º—É–º –≤–æ–∑–º–æ–∂–Ω—ã—Ö —Å–∏–º–≤–æ–ª–æ–≤

# a+? ‚Äî –º–∏–Ω–∏–º–∞–ª—å–Ω–æ–µ –∫–æ–ª–∏—á–µ—Å—Ç–≤–æ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–π (–Ω–µ–∂–∞–¥–Ω—ã–π —Ä–µ–∂–∏–º)
print(re.findall(r"a+?", "aaaaa"))  # ['a', 'a', 'a', 'a', 'a']
# –æ–±—ä—è—Å–Ω–µ–Ω–∏–µ: –≤–º–µ—Å—Ç–æ –æ–¥–Ω–æ–π –±–æ–ª—å—à–æ–π –≥—Ä—É–ø–ø—ã –≤–µ—Ä–Ω—ë—Ç –º–Ω–æ–≥–æ –ø–æ –æ–¥–Ω–æ–π "a"

# ab|cd ‚Äî —Å–æ–≤–ø–∞–¥–∞–µ—Ç —Å "ab" –∏–ª–∏ "cd"
print(re.findall(r"ab|cd", "abxxcdyyabzz"))  # ['ab', 'cd', 'ab']
# –æ–±—ä—è—Å–Ω–µ–Ω–∏–µ: –∞–ª—å—Ç–µ—Ä–Ω–∞—Ü–∏—è, –≤—ã–±–∏—Ä–∞–µ—Ç –æ–¥–Ω–æ –∏–∑ –≤—ã—Ä–∞–∂–µ–Ω–∏–π


# ^abc$ ‚Äî —à–∞–±–ª–æ–Ω, –∫–æ—Ç–æ—Ä—ã–π –ø—Ä–æ–≤–µ—Ä—è–µ—Ç, —Å–æ–≤–ø–∞–¥–∞–µ—Ç –ª–∏ –≤—Å—è —Å—Ç—Ä–æ–∫–∞ –¶–ï–õ–ò–ö–û–ú —Å "abc"
# üîπ –ü—Ä–∏–º–µ—Ä 1: —Ç–æ—á–Ω–æ–µ —Å–æ–≤–ø–∞–¥–µ–Ω–∏–µ
print(bool(re.match(r"^abc$", "abc")))      # True
# –æ–±—ä—è—Å–Ω–µ–Ω–∏–µ: —Å—Ç—Ä–æ–∫–∞ –Ω–∞—á–∏–Ω–∞–µ—Ç—Å—è (^) —Å 'a', –ø—Ä–æ–¥–æ–ª–∂–∞–µ—Ç—Å—è 'b', –∑–∞–∫–∞–Ω—á–∏–≤–∞–µ—Ç—Å—è ($) 'c' ‚Äî –≤—Å—ë —Å–æ–≤–ø–∞–¥–∞–µ—Ç

# üîπ –ü—Ä–∏–º–µ—Ä 2: –ª–∏—à–Ω–∏–π —Å–∏–º–≤–æ–ª –≤ –Ω–∞—á–∞–ª–µ
print(bool(re.match(r"^abc$", "xabc")))     # False
# –æ–±—ä—è—Å–Ω–µ–Ω–∏–µ: ^ —Ç—Ä–µ–±—É–µ—Ç, —á—Ç–æ–±—ã —Å—Ç—Ä–æ–∫–∞ –ù–ê–ß–ò–ù–ê–õ–ê–°–¨ —Å 'a', –∞ –∑–¥–µ—Å—å —Å–Ω–∞—á–∞–ª–∞ 'x'


text = "A1_b c9#"
# .  ‚Äî –ª—é–±–æ–π –û–î–ò–ù —Å–∏–º–≤–æ–ª, –∫—Ä–æ–º–µ –ø–µ—Ä–µ–≤–æ–¥–∞ —Å—Ç—Ä–æ–∫–∏
print(re.findall(r".", text))  
# ['A', '1', '_', 'b', ' ', 'c', '9', '#']

# \w ‚Äî "word character" ‚Üí –ª—é–±–∞—è –±—É–∫–≤–∞, —Ü–∏—Ñ—Ä–∞ –∏–ª–∏ _
print(re.findall(r"\w", text))
# ['A', '1', '_', 'b', 'c', '9']

# \d ‚Äî "digit" ‚Üí –ª—é–±–∞—è —Ü–∏—Ñ—Ä–∞ –æ—Ç 0 –¥–æ 9
print(re.findall(r"\d", text))
# ['1', '9']



