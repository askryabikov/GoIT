# ## Задание: Операции с матрицами

# ### Условие
# 1) Создайте две матрицы `A` и `B` размером **3×3** со случайными целыми числами от **1 до 10** (включительно), используя:
# `np.random.randint(1, 11, (3, 3))`.

# 2) Выполните и выведите результаты следующих операций:
# - **A + B** (сложение матриц)
# - **A - B** (вычитание матриц)
# - **3 * A** (умножение первой матрицы на скаляр 3)
# - Линейная комбинация: **2*A + 3*B**

# ### Требования к выводу
# - Сначала выведите `A` и `B`.
# - Затем отдельно выведите результат каждой операции с понятной подписью.


import numpy as np

# Дві матриці 3×3
A = np.random.randint(1, 11, (3, 3))
B = np.random.randint(1, 11, (3, 3))

print("Матриця A:")
print(A)
print("Матриця B:")
print(B)

# Додавання
C = A + B
print("A + B:")
print(C)

# Віднімання
D = A - B
print("A - B:")
print(D)

# Множення на скаляр
E = 3 * A
print("3 × A:")
print(E)

# Комбінована операція
F = 2*A + 3*B
print("2×A + 3×B:")
print(F)