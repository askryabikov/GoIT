# Перевірка колінеарності

import numpy as np

def is_collinear(u, v):
    u1, u2 = u
    v1, v2 = v

    # если первая компонента u1 = 0, то по первой компоненте λ не найти
    # тогда условие коллинеарности: v1 тоже должен быть 0,
    # и λ можно искать по второй компоненте
    if u1 == 0:
        if v1 != 0:
            return False
        # теперь проверяем по второй компоненте: v2 = λ*u2
        # если u2 тоже 0, то коллинеарны только если v2 тоже 0
        return (u2 == 0 and v2 == 0) or (u2 != 0)  # v1=0 уже выполнено

    # λ = v1 / u1 (логически), проверяем на второй компоненте без деления:
    return v2 * u1 == u2 * v1


# тесты из задания
print(is_collinear([2, 6], [1, 3]))     # True
print(is_collinear([3, -1], [6, 2]))    # False










