# Задача 3. Симметрическая разность и декартово произведение

# Дано:
# A = {1, 2, 3, 4}
# B = {3, 4, 5, 6}

# Нужно:
# 1) Вычислить симметрическую разность множеств A и B.
# 2) Вычислить декартово произведение A × {x, y}.


from itertools import product

A = {1, 2, 3, 4}
B = {3, 4, 5, 6}

# 1) Симметрическая разность: элементы, которые есть только в одном из множеств
sym_diff = A.symmetric_difference(B)   # или A ^ B
print("1) A Δ B:", sym_diff)

# 2) Декартово произведение A × {x, y}
X = {'x', 'y'}
cartesian = set(product(A, X))         # пары (a, x) и (a, y)
print("2) A × {x, y}:", cartesian)
