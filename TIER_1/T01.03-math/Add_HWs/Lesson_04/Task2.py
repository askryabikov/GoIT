# Задача 2. Подмножества и равенство множеств

# Дано:
# A = {1, 2, 3, 4}
# B = {2, 3}
# C = {4, 3, 2, 1}
# D = ∅

# Нужно:
# 1) Проверить, верно ли B ⊆ A.
# 2) Проверить, верно ли A = C.
# 3) Проверить, верно ли D ⊆ A.


A = {1, 2, 3, 4}
B = {2, 3}
C = {4, 3, 2, 1}
D = set ()

# B ⊆ A
print("1) B ⊆ A:", B.issubset(A))   # True

# A = C
print("2) A = C:", A == C)          # True

# D ⊆ A
print("3) D ⊆ A:", D.issubset(A))   # True