# ## Задача: Векторное произведение

# ### Дано
# u = [1, 2, 3]  
# v = [2, 4, 6]

# ### Нужно
# 1) Вычислить векторное произведение `u × v`.  
# 2) Ответить на вопрос: что можно сказать про векторы `u` и `v`, 
# если результат равен нулю?


import numpy as np

# Вектори
u = np.array([1, 2, 3])
v = np.array([2, 4, 6])

# Через NumPy
w = np.cross(u, v)

print(f"Вектор u: {u}")
print(f"Вектор v: {v}")
print(f"Векторний добуток (NumPy): {w}")

# Перевірка ортогональності
print(f"Ортогональність:")
print(f"  u · (u×v) = {u @ w}")
print(f"  v · (u×v) = {v @ w}")

# Вектор u: [1 2 3]
# Вектор v: [2 4 6]
# Векторний добуток (NumPy): [0 0 0]
# Ортогональність:
#   u · (u×v) = 0
#   v · (u×v) = 0


