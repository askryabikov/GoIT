# ## Задача: Проєкція вектора на вектор

# ### Дано
# a = [3, 4]  
# b = [2, 0]

# ### Потрібно
# 1) Знайти проєкцію вектора `a` на вектор `b`.  
# 2) Відповісти на питання: як зміниться проєкція, 
# якщо `b` нормалізувати перед обчисленням?


import numpy as np

# Два вектори
a = np.array([3, 4])
b = np.array([2, 0])

print(f"Вектор a = {a}")
print(f"Вектор b = {b}")

# Обчислюємо проєкцію a на b
proj_b_a = ((a @ b) / (b @ b)) * b

print(f"  Проєкція a на b:")
print(f"  proj_b(a) = {proj_b_a}")
print(f"  Довжина проєкції: {np.linalg.norm(proj_b_a):.4f}")

# 2) Якщо нормалізувати b перед обчисленням
b_hat = b / np.linalg.norm(b)      # нормалізований b (одиничний)
proj_bhat_a = (a @ b_hat) * b_hat  # proj_{b̂}(a) = (a·b̂) b̂

print("2) Проєкція a на нормалізований b̂:")
print(f"  b̂ = {b_hat}")
print(f"  proj_b̂(a) = {proj_bhat_a}")
print(f"  Однакові проєкції? {np.allclose(proj_b_a, proj_bhat_a)}")

print("Висновок:")
print("  Векторна проєкція не змінюється при нормалізації b, " 
"бо формула компенсує зміну довжини b.")

