import numpy as np

# Dataset: 5 людей, 3 ознаки (зріст, вага, вік)*
data = np.array([
    [170, 70, 25],  # людина 0*
    [165, 60, 30],  # людина 1*
    [180, 80, 22],  # людина 2*
    [175, 75, 28],  # людина 3*
    [160, 55, 35]   # людина 4*
])

print("Dataset:")
print(data)
print(f"Розмірність: {data.shape[0]} зразків (людей) × {data.shape[1]} ознак")


# Dataset:
# [[170  70  25]
#  [165  60  30]
#  [180  80  22]
#  [175  75  28]
#  [160  55  35]]

# Розмірність: 5 зразків (людей) × 3 ознак

# Дані про першу людину перший рядок
print(f"Перша людина: {data[0, :]}")
print(f"  Зріст: {data[0, 0]} см")
print(f"  Вага: {data[0, 1]} кг")
print(f"  Вік: {data[0, 2]} років")

# Зріст всіх людей перший стовпець
heights = data[:, 0]
print(f"Зріст всіх людей: {heights}")
print(f"Середній зріст людини: {heights.mean():.1f} см")

# Вага кожної людини другий стовпець
weights = data[:, 1]
print(f"Ваги кожної людини: {weights}")
print(f"Середня вага людини: {weights.mean():.1f} кг")


# Перша людина: [170  70  25]
#   Зріст: 170 см
#   Вага: 70 кг
#   Вік: 25 років

# Зріст всіх людей: [170 165 180 175 160]
# Середній зріст людини: 170.0 см

# Ваги кожної людини: [70 60 80 75 55]
# Середня вага людини: 68.0 кг

