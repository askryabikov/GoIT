import numpy as np

# Матриця 2×3
A = np.array([[1, 2, 3],
              [4, 5, 6]])

# Вектор розмірності 3
x = np.array([1, 0, 2])

# Множення матриці на вектор
b = A @ x

print("Матриця A (2×3):")
print(A)
print(f"Вектор x (3,):")
print(x)
print(f"Результат A @ x (2,):")
print(b)

# Матриця A (2×3):
# [[1 2 3]
#  [4 5 6]]

# Вектор x (3,):
# [1 0 2]

# Результат A @ x (2,):
# [ 7 16]