import numpy as np

# Створюємо матрицю 3×2
A = np.array([[1, 2],
              [3, 4],
              [5, 6]])

print("Оригінальна матриця A (3×2):")
print(A)

# Транспонуємо
A_T = A.T

print("Транспонована A^T (2×3):")
print(A_T)

print(f"Розмірність A: {A.shape}")
print(f"Розмірність A^T: {A_T.shape}")


# Оригінальна матриця A (3×2):
# [[1 2]
#  [3 4]
#  [5 6]]

# Транспонована A^T (2×3):
# [[1 3 5]
#  [2 4 6]]

# Розмірність A: (3, 2)
# Розмірність A^T: (2, 3)