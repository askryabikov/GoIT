import numpy as np

# Матриці з прикладу
A = np.array([[1, 2, 3],
              [4, 5, 6]])  # 2×3

B = np.array([[7, 8],
              [9, 10],
              [11, 12]])  # 3×2

# Множення A @ B
C = A @ B

print("Матриця A (2×3):")
print(A)
print("Матриця B (3×2):")
print(B)
print(f"Результат C = A @ B ({C.shape[0]}×{C.shape[1]}):")
print(C)


# Матриця A (2×3):
# [[1 2 3]
#  [4 5 6]]

# Матриця B (3×2):
# [[ 7 8]
#  [ 9 10]
#  [11 12]]

# Результат C = A @ B (2×2):
# [[ 58 64]
#  [139 154]]
