import numpy as np

# Припустимо, ми зібрали дані про 1000 користувачів
# Час у застосунку в хвилинах

data = np.random.uniform(10, 60, 1000)  # для прикладу генеруємо випадкові дані

# Вибіркове середнє
sample_mean = np.mean(data)
print(f"Вибіркове середнє: {sample_mean:.2f} хвилин")

# Вибіркова дисперсія
# ddof=1 означає "ділимо на n-1"
sample_variance = np.var(data, ddof=1)
print(f"Вибіркова дисперсія: {sample_variance:.2f} хвилин²")

# Стандартне відхилення
sample_std = np.std(data, ddof=1)
print(f"Стандартне відхилення: {sample_std:.2f} хвилин")


# Вибіркове середнє: 34.51 хвилин
# Вибіркова дисперсія: 213.36 хвилин²
# Стандартне відхилення: 14.61 хвилин


# Функція np.mean(data) рахує середнє арифметичне всіх чисел у масиві data.

# Функція np.var(data, ddof=1) рахує дисперсію, 
# де параметр ddof=1 означає, що ділимо на n-1 замість n.

# Функція np.std(data, ddof=1) рахує стандартне відхилення - 
# це еквівалентно np.sqrt(np.var(data, ddof=1)).