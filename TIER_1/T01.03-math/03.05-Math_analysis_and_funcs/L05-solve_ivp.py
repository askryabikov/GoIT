# Параметры функции `solve_ivp`

# `fun` — функция правой части дифференциального уравнения  
# dy/dt = f(t, y).  
# Она должна принимать два аргумента: время `t` (скаляр) и текущее значение `y` (массив), и возвращать производную `dy/dt` (массив той же размерности). Даже для одной переменной `y` должен быть массивом.

# `t_span` — кортеж `(t_start, t_end)`, задающий интервал времени решения.  
# Например, `(0, 10)` означает решение от `t = 0` до `t = 10`.

# `y0` — начальное значение.  
# Должно быть списком или массивом. Даже для одной переменной нужно писать `[y0]`, а не просто `y0`.

# `dense_output` — если `True`, функция сохраняет интерполяционную функцию, которая позволяет получать значения решения в любой момент времени внутри `t_span`. Если `dense_output` не задан (или `False`), решение будет доступно только в тех точках, которые метод выбрал автоматически.

# ------------------------------------------------------------

# Функция возвращает объект `solution`, который содержит:

# `solution.t` — массив моментов времени, в которых метод вычислил решение.

# `solution.y` — массив значений решения  
# (для одной переменной это массив размерности `(1, n)`).

# `solution.sol` — интерполяционная функция (доступна, если `dense_output=True`).  
# Если вызвать `solution.sol(t_values)`, можно получить значения решения в заданных точках `t_values`.




