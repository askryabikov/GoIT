import numpy as np

# Система: 2x₁ + 3x₂ = 8
#          x₁ - x₂ = 1

A = np.array([
    [2, 3],
    [1, -1]
])

b = np.array([8, 1])

print("Матриця коефіцієнтів A:")
print(A)
print(f"Вектор правої частини b: {b}")

# Розв'язуємо систему
x = np.linalg.solve(A, b)

print(f"Розв'язок системи x: {x}")
print(f"  x₁ = {x[0]}")
print(f"  x₂ = {x[1]}")

# Перевірка: A @ x має дорівнювати b
result = A @ x
print(f"Перевірка A @ x = {result}")

