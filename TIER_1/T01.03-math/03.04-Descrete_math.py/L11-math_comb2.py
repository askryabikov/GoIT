# Якщо потрібно отримати всі можливі варіанти
#  сполучень, використовуємо itertools.combinations(iterable, r).

from itertools import combinations

developers = ['Анна', 'Іван', 'Марія', 'Петро', 'Ольга']

# Генеруємо всі можливі трійки
all_teams = list(combinations(developers, 3))

print(f"Всього варіантів команд: {len(all_teams)}")
for i, team in enumerate(all_teams, 1):
    print(f"{i}. {', '.join(team)}")


