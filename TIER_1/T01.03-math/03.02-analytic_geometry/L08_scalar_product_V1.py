# Скалярний добуток у NumPy можна обчислити кількома способами, 
# але на практиці використовують два основних.

# Перший, через функцію np.dot(). 
# Вона явно показує, що відбувається множення елементів 
# і підсумовування:


import numpy as np

u = np.array([2, 3])
v = np.array([4, -1])

dot = np.dot(u, v)

print(f"Скалярний добуток: {dot}")

# Скалярний добуток: 5
