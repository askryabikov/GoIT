# Скалярний добуток у NumPy можна обчислити кількома способами, 
# але на практиці використовують два основних.
# Другий, більш зручний, вже відомий нам оператор @. 
# Він робить те саме, але коротше:

import numpy as np

u = np.array([2, 3])
v = np.array([4, -1])

dot = u @ v
print(f"Скалярний добуток: {dot}")

# Скалярний добуток: 5

