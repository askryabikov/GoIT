# У Netflix або Spotify користувачі представлені векторами оцінок. 
# Щоб знайти схожих користувачів, обчислюють косинусну схожість між їхніми векторами.

# Припустимо, що маємо дані про вподобання 3 користувачів щодо 5 фільмів.

# Оцінка виставляється за шкалою від 1 до 5, а 0 означає, 
# що користувач цей фільм не дивився.

import numpy as np

def cosine_similarity(u, v):
    return (u @ v) / (np.linalg.norm(u) * np.linalg.norm(v))

# Оцінки користувачів (5 фільмів)
user1 = np.array([5, 4, 0, 0, 1])  # любить перші два фільми
user2 = np.array([4, 5, 0, 1, 0])  # схожі смаки
user3 = np.array([1, 0, 5, 4, 5])  # зовсім інші смаки

print("Користувачі:")
print(f"  User 1: {user1}")
print(f"  User 2: {user2}")
print(f"  User 3: {user3}")

# Обчислюємо схожість
sim_12 = cosine_similarity(user1, user2)
sim_13 = cosine_similarity(user1, user3)

print(f"Косинусна схожість User1 vs User2: {sim_12:.4f}")
print(f"Косинусна схожість User1 vs User3: {sim_13:.4f}")

# Користувачі:
#   User 1: [5 4 0 0 1]
#   User 2: [4 5 0 1 0]
#   User 3: [1 0 5 4 5]

# Косинусна схожість User1 vs User2: 0.9524
# Косинусна схожість User1 vs User3: 0.1885


# В нашому прикладі кожен користувач — це вектор його оцінок. 
# Якщо користувачі ставлять високі оцінки одним і тим самим фільмам, 
# їхні вектори "дивляться” в один бік, і косинус між ними близький до 1.

# Ми бачимо, що User 1 і User 2 мають близькі напрямки 0.95, отже, 
# вони ставлять подібні оцінки тим самим фільмам. 
# User 3 має дуже малу схожість із ними, тому його вподобання інші. 
# Таким чином, User 2 — найближчий за смаками до User 1, 
# і йому можна рекомендувати ті фільми, які сподобаються першому користувачу.

# Косинусна схожість дозволяє знаходити користувачів зі схожими 
# вподобаннями навіть якщо вони оцінили різну кількість фільмів. 
# Це робить її ідеальною метрикою для рекомендаційних систем.