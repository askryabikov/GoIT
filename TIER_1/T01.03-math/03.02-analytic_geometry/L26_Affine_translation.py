# ## Чистий зсув (translation) на вектор [2, 1]

# Розглядаємо зсув на вектор:
# b = [2, 1]

# Квадрат, який початково знаходиться біля початку координат, 
# переміщується на:
# - 2 одиниці вправо
# - 1 одиницю вгору

# Це афінне перетворення, де матриця A — одинична:
# A = I

# Уся дія задається лише вектором зсуву b.



import numpy as np
import matplotlib.pyplot as plt

# Зсув на вектор [2, 1]
b = np.array([2, 1])

# Квадрат
square = np.array([
    [0, 1, 1, 0, 0],
    [0, 0, 1, 1, 0]
])

# Афінне перетворення: T(v) = I*v + b 
square_translated = square + b[:, np.newaxis]

print(f"Вектор зсуву: {b}")
print(f"Початковий квадрат (координати вершин):")
print(square)
print(f"Квадрат після зсуву (координати вершин):")
print(square_translated)


# Вектор зсуву: [2 1]

# Початковий квадрат (координати вершин):
# [[0 1 1 0 0]
#  [0 0 1 1 0]]

# Квадрат після зсуву (координати вершин):
# [[2 3 3 2 2]
#  [1 1 2 2 1]]

