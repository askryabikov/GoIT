
# Библиотека NumPy предоставляет удобный способ проверки 
# линейной независимости из-за вычисления ранга матрицы, 
# которое мы тоже рассматривали.

# Если мы составим матрицу из наших векторов как столбцов, 
# то ее ранг покажет, сколько из них независимых:


import numpy as np

# Три вектори в R²
v1 = np.array([1, 0])
v2 = np.array([0, 1])
v3 = np.array([2, 3])

# Складаємо матрицю з векторів як стовпців
A = np.column_stack([v1, v2, v3])
print("Матриця з векторів:")
print(A)

rank = np.linalg.matrix_rank(A)
print(f"Ранг матриці: {rank}")
print(f"Кількість векторів: {A.shape[1]}")

if rank < A.shape[1]:
    print(f"Вектори лінійно залежні")
    print(f"Лінійно незалежних серед них: {rank}")
else:
    print(f"Вектори лінійно незалежні")


# Матриця з векторів:
# [[1 0 2]
#  [0 1 3]]

# Ранг матриці: 2
# Кількість векторів: 3
# Вектори лінійно залежні
# Лінійно незалежних серед них: 2

