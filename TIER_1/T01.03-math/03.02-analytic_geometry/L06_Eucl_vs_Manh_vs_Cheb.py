import numpy as np

A = np.array([2, 3])
B = np.array([5, 7])

# Різниця векторів
diff = B - A
print(f"Точка A: {A}")
print(f"Точка B: {B}")
print(f"Різниця B - A: {diff}")

# Різні відстані
d_L2 = np.linalg.norm(diff, ord=2)
d_L1 = np.linalg.norm(diff, ord=1)
d_Linf = np.linalg.norm(diff, ord=np.inf)

print(f"Евклідова відстань: {d_L2:.2f}")
print(f"Мангеттенська відстань: {d_L1:.2f}")
print(f"Чебишова відстань: {d_Linf:.2f}")


# Точка A: [2 3]
# Точка B: [5 7]
# Різниця B - A: [3 4]

# Евклідова відстань: 5.00
# Мангеттенська відстань: 7.00
# Чебишова відстань: 4.00


# Разница между метриками L2, L1 и L∞ (Chebyshev)

# Пусть A и B — два вектора (точки). Обозначим разность:
# Δ = B - A = (d1, d2, ..., dn)

# 1) Евклидова дистанция (L2)
# ||Δ||2 = sqrt(d1^2 + d2^2 + ... + dn^2)

# Смысл:
# - “Прямая линия” между точками (обычная геометрическая длина).
# - Сильнее штрафует большие отклонения (из-за квадратов).

# Когда уместна:
# - геометрия, физика, kNN/кластеризация “по расстоянию”
# - когда большие ошибки надо наказывать сильнее.

# 2) Манхэттенская дистанция (L1)
# ||Δ||1 = |d1| + |d2| + ... + |dn|

# Смысл:
# - “По решётке”: суммарный путь вдоль осей.
# - Штраф линейный → обычно устойчивее к выбросам, чем L2.

# Когда уместна:
# - когда важна суммарная ошибка по координатам
# - в задачах, где выбросы не должны сильно ломать метрику.

# 3) Дистанция Чебышёва (L∞)
# ||Δ||∞ = max(|d1|, |d2|, ..., |dn|)

# Смысл:
# - “Худший случай”: только максимальное отклонение по одной координате.
# - Остальные координаты не важны, если они не максимальные.

# Когда уместна:
# - контроль качества/ограничения: “ни одна ошибка не должна превышать порог”
# - задачи worst-case.

# Пример, чтобы увидеть разницу
# Δ = (3, 4)
# L1 = 3 + 4 = 7
# L2 = sqrt(3^2 + 4^2) = 5
# L∞ = max(3, 4) = 4

# Когда разница становится существенной
# - Если есть выброс (одна компонента очень большая): L2 и L∞ почти полностью определяются выбросом.
# - Если много мелких отклонений vs одно большое:
#   L1 смотрит на сумму,
#   L2 сильнее реагирует на крупные значения,
#   L∞ берёт только максимум.
# - Если важно “суммарно” vs “максимально”: выбирай L1 vs L∞ соответственно.

