# Вектори ортогональні — тобто взаємно перпендикулярні.


import numpy as np

# Приклад 1: Стандартні базисні вектори
e1 = np.array([1, 0, 0])
e2 = np.array([0, 1, 0])
e3 = np.array([0, 0, 1])

print("Стандартні базисні вектори:")
print(f"  e₁ = {e1}")
print(f"  e₂ = {e2}")
print(f"  e₃ = {e3}")
print(f"e₁ · e₂ = {e1 @ e2}")
print(f"e₁ · e₃ = {e1 @ e3}")
print(f"e₂ · e₃ = {e2 @ e3}")
print("Усі три вектори попарно ортогональні")


