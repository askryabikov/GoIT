# Це простий приклад алгоритму nearest neighbor, 
# який є одним з базових методів машинного навчання.

# Ідея проста — знайти в датасеті точки, найближчі до нового об'єкта, 
# і використати їх для прийняття рішення.



import numpy as np

# Координати міст (широта, довгота) - звісно спрощено
cities = np.array([
    [50.45, 30.52],   # Київ
    [48.29, 25.94],   # Чернівці
    [49.84, 24.03],   # Львів
    [46.48, 30.73],   # Одеса
    [49.99, 36.23]    # Харків
])

city_names = ['Київ', 'Чернівці', 'Львів', 'Одеса', 'Харків']

# Наша поточна позиція де-не-будь в районі Вінниці ;)
P = np.array([49.23, 28.47])

print(f"Поточна локація P: {P}")
print("Відстані до міст:")

# Обчислюємо відстані до всіх міст
distances = []
for i, city_coords in enumerate(cities):
    dist = np.linalg.norm(city_coords - P)
    distances.append(dist)
    print(f"  {city_names[i]}: {dist:.2f}")

# Знаходимо найближче місто
nearest_idx = np.argmin(distances)
print(f"Найближче місто: {city_names[nearest_idx]}")
print(f"Відстань: {distances[nearest_idx]:.2f}")


# Поточна локація P: [49.23 28.47]

# Відстані до міст:
#   Київ: 2.39
#   Чернівці: 2.70
#   Львів: 4.48
#   Одеса: 3.56
#   Харків: 7.80

# Найближче місто: Київ
# Відстань: 2.39
