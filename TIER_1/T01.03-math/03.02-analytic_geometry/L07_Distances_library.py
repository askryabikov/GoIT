# Для багатьох точок зручно використовувати готові функції з бібліотеки scipy.spatial.distance, 
# де реалізовано всі основні метрики.

import numpy as np

from scipy.spatial import distance

A = np.array([2, 3])
B = np.array([5, 7])

# Різні метрики через scipy
euclidean = distance.euclidean(A, B)
manhattan = distance.cityblock(A, B)  # cityblock = Manhattan
chebyshev = distance.chebyshev(A, B)  # Chebyshev

print(f"Евклідова: {euclidean:.2f}")
print(f"Мангеттенська: {manhattan:.2f}")
print(f"Чебишова: {chebyshev:.2f}")


# Евклідова: 5.00
# Мангеттенська: 7.00
# Чебишова: 4.00


# Как считаются расстояния (L2 / L1 / L∞)

# Пусть есть две точки (вектора) A = (a1, a2, ..., an) и B = (b1, b2, ..., bn).
# Сначала считаем разность по координатам:
# Δ = B - A = (b1 - a1, b2 - a2, ..., bn - an)

# 1) Евклидово расстояние (L2)
# d2(A, B) = sqrt( Σ (bi - ai)^2 )
# То есть:
# - вычесть координаты,
# - возвести разности в квадрат,
# - сложить,
# - взять корень.

# 2) Манхэттенское расстояние (L1)
# d1(A, B) = Σ |bi - ai|
# То есть:
# - вычесть координаты,
# - взять модуль каждой разности,
# - сложить.

# 3) Расстояние Чебышёва (L∞)
# d∞(A, B) = max_i |bi - ai|
# То есть:
# - вычесть координаты,
# - взять модуль каждой разности,
# - взять максимальную.

# Короткий пример:
# A = (1, 2), B = (4, 6)
# Δ = (3, 4)
# L2 = sqrt(3^2 + 4^2) = 5
# L1 = |3| + |4| = 7
# L∞ = max(|3|, |4|) = 4
